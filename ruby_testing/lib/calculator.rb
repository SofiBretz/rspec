# frozen_string_literal: true

class Calculator
  def add_sum1(a, b)
    a + b
  end

  def add_sum2(a, b, c)
    a + b + c
  end

  def add_mult1(a, b)
    a * b
  end

  def add_mult2(a, b, c)
    a * b * c
  end

  def add_subs1(a, b)
    a / b
  end

  def add_subs2(a, b, c)
    a / b / c
  end
end
